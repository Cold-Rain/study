'''
Задание №4
Преобразовать строку в число
Компания: «СБЕР»
Уровень: Средний

Пользователь с клавиатуры (через функцию input()) вводит строку — описание числа словами на русском языке.
Напишите функцию transform_string_to_integer(), которая принимает на вход введённую пользователем строку,
конвертирует слова в число и возвращает число, которое описано в строке s.
Примечания:
Минимальное число — ноль (0) включительно. Максимальное число — один миллион (1000000) включительно.
Гарантируется, что на вход подаются строки с описанием числа, проверять это не нужно.
Пример №1:
Ввод:
один
Вывод:
1
Пример №2:
Ввод:
двадцать
Вывод:
20
Пример №3:
Ввод:
двести сорок шесть
Вывод:
246
Пример №4:
Ввод:
семьсот восемьдесят три тысячи девятьсот девятнадцать
Вывод:
783919
При решении задачи вам может пригодиться метод строк s.startswith(word), который определяет, начинается ли строка s со строки word.
Подробнее о нём вы можете узнать в документации.
'''
# Словарь для сопоставления слов с числовыми значениями
# Словарь для сопоставления слов с числовыми значениями
number_word_dict = {
    "тысяч": 1000, "миллион": 1000000, "тысячи": 1000, "тысяча": 1000,
    "сто": 100, "двести": 200, "триста": 300, "четыреста": 400, "пятьсот": 500, "шестьсот": 600, "семьсот": 700, "восемьсот": 800, "девятьсот": 900,
    "одиннадцать": 11, "двенадцать": 12, "тринадцать": 13, "четырнадцать": 14, "пятнадцать": 15, "шестнадцать": 16, "семнадцать": 17, "восемнадцать": 18, "девятнадцать": 19,
    "двадцать": 20, "тридцать": 30, "сорок": 40, "пятьдесят": 50, "шестьдесят": 60, "семьдесят": 70, "восемьдесят": 80, "девяносто": 90,
    "десять": 10, "ноль": 0, "один": 1, "два": 2, "три": 3, "четыре": 4, "пять": 5, "шесть": 6, "семь": 7, "восемь": 8, "девять": 9,
}

def transform_string_to_integer(input_string):
    # Разбиваем строку на слова
    words = input_string.split()
    # Инициализируем переменные для хранения текущего числа и результата
    current_number = 0
    total_number = 0

    for word in words:
        if word in number_word_dict:
            # Если слово является тысячами или миллионами, добавляем текущее число к результату и сбрасываем текущее число
            if word in ["тысяч", "миллион", "тысячи", "тысяча"]:
                total_number += current_number * number_word_dict[word]
                current_number = 0
            else:
                # Иначе добавляем значение слова к текущему числу
                current_number += number_word_dict[word]
        else:
            # Если слово не найдено в словаре, возвращаем ошибку
            return "Некорректный ввод"

    # Добавляем оставшееся текущее число к результату
    total_number += current_number
    return total_number

# Ввод данных от пользователя
input_string = input('Введите число словами: ')
# Вызов функции и вывод результата
number = transform_string_to_integer(input_string)
print(number)

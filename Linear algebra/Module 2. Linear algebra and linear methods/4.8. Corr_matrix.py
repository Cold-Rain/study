'''
Составьте корреляционную матрицу для системы векторов:
Для расчёта используйте библиотеку NumPy или Pandas.
1. чему равен ранг
2. Чему равен определитель полученной корреляционной матрицы?
 Ответ округлите до седьмого знака после точки-разделителя.
'''
import numpy as np

# Данные векторов
x1 = np.array([5.1, 1.8, 2.1, 10.3, 12.1, 12.6]).T
x2 = np.array([10.2, 3.7, 4.1, 20.5, 24.2, 24.1]).T
x3 = np.array([2.5, 0.9, 1.1, 5.1, 6.1, 6.3]).T

# Формируем матрицу данных
data = np.vstack([x1, x2, x3])

# Вычисление корреляционной матрицы
correlation = np.corrcoef(data)

# Вычисление ранга корреляционной матрицы
rank = np.linalg.matrix_rank(correlation)

# Вычисление определителя корреляционной матрицы
determinant = np.linalg.det(correlation)

# Настроим вывод, чтобы не использовать экспоненциальный формат
np.set_printoptions(precision=7, suppress=True)  # suppress=True отключает экспоненциальное представление

print("Корреляционная матрица:")
print(correlation)
print("\nРанг корреляционной матрицы:", rank)
print("\nОпределитель корреляционной матрицы:", determinant)
